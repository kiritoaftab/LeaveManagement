    private void fillMemo()  {
         // Generated from nbfs://nbhost/SystemFileSystem/Templates/Classes/Code/GeneratedMethodBody
        try{
         File member=new File("login_details.txt");
         
         String reader;
        BufferedReader br_member=new BufferedReader(new FileReader(member));
        while((reader=br_member.readLine())!=null){
            String str[]=reader.split("\\,");
            if(str.length <=2) continue;
            String id1=str[1];
            String pass1=str[2];
            Memoisation.map.put(id1, pass1);
            
        }
        br_member.close();
        File leave=new File("leave_data.txt");
        BufferedReader br_leave=new BufferedReader(new FileReader(leave));
        while((reader=br_leave.readLine())!=null){
            String str[]=reader.split("\\,");
            if(str.length <=7) continue;
            String id1=str[1];
            String l1=str[2];
            String l2=str[3];
            String l3=str[4];
            String l4=str[5];
            String l5=str[6];
            String b= l1+","+l2+","+l3+","+l4+","+l5+",";
            Memoisation.leaveData.put(id1, b);
            
            
        }
        br_leave.close();
        }catch(FileNotFoundException e){
            System.out.println("in fillMemo()");
            System.out.println("Files not created yet, fresh records");
        }catch(IOException e){
            System.out.println("Stream is getting flused, Please wait");
            e.printStackTrace();
        }
    }
